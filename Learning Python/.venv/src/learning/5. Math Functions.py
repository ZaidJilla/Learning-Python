import math

pi = 3.14
# round to the nearest integer
print(round(pi)) 
# round up
print(math.ceil(pi))
# round down
print(math.floor(pi))
# absolute value
print(abs(pi))
# power
print(pow(pi, 2)) # pi^3
# square root
print(math.sqrt(pi))

